// LINK : https://codeforces.com/problemset/problem/1374/A

#include <bits/stdc++.h>
using namespace std;
using ll = long long;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    if (!(cin >> t)) return 0;
    while (t--) {
        ll x, y, n;
        cin >> x >> y >> n;
        ll rem = (n - y) % x;
        ll k = n - rem;
        cout << k << '\n';
    }
    return 0;
}
