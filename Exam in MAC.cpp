void TC() {
    ll n , c;
    cin >> n >> c;
    ll ans = (c + 1) * (c + 2) / 2;
    vector<ll>a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    int odd = 0  , even = 0;
    for (int i = 0; i < n; ++i) {
        ans -= (c - a[i] + 1);
        ans -= a[i] / 2 + 1;
        if(a[i] & 1){
            odd++;
            ans += odd;
        }else{
            even++;
            ans += even;
        }
    }
    cout << ans << nl;
}
