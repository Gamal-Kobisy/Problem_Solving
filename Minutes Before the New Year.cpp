#include <bits/stdc++.h>
using namespace std;
using ll = long long;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;
    while (t--) {
        int h, m;
        cin >> h >> m;

        int total_minutes = 24 * 60;
        int passed = h * 60 + m;
        cout << total_minutes - passed << '\n';
    }

    return 0;
}
