void TC() {
    ll n, x, y;
    string s;
    cin >> n >> x >> y >> s;
    vector<ll> a(n);
    ll sum = 0;
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
        sum += a[i];
    }
    if (sum > x + y) return void(no);
    ll x_need = 0, y_need = 0;
    bool has_zero = false, has_one = false;
    for (int i = 0; i < n; ++i) {
        if (s[i] == '0') {
            x_need += a[i] / 2 + 1;
            has_zero = true;
        } else {
            y_need += a[i] / 2 + 1;
            has_one = true;
        }
    }
    if (x_need > x || y_need > y) return void(no);
    if (!has_one) {
        if (x >= y + n) yes;
        else no;
    } else if (!has_zero) {
        if (y >= x + n) yes;
        else no;
    } else {
        yes;
    }
}
