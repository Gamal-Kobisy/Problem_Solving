void TC() {
    int n, m;
    cin >> n >> m;
    vector<int>a(n), b(m);
    vector<bool>ok(n, true);
    map<int , int>idx;
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
        idx[a[i]] = i;
    }
    for (int i = 0; i < m; ++i) {
        cin >> b[i];
        ok[idx[b[i]]] = false;
    }
    int ans = 1;
    for (int i = 0; i < m; ++i) {
        int cnt = 0;
        if(idx[b[i]] > 0) cnt += ok[idx[b[i]] - 1];
        if(idx[b[i]] < n - 1) cnt += ok[idx[b[i]] + 1];
        ans = mul(ans , cnt);
        ok[idx[b[i]]] = true;
    }
    cout << ans << nl;
}
