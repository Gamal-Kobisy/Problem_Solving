int n , m ,memo[5005][5005];
string s , t;
int solve(int idx1 , int idx2){
    if(idx1 < 0 or idx2 < 0) return 0;
    int &res = memo[idx1][idx2];
    if(~res) return res;
    res = 0;
    if(s[idx1] == t[idx2]){
        res = max(res , solve(idx1 - 1 , idx2 - 1) + 2);
    }
    res = max(res , max(solve(idx1 - 1 , idx2)
             ,solve(idx1 , idx2 - 1)) - 1);
    return res;
}

void TC() {
    cin >> n >> m;
    cin >> s >> t;
    mem(memo , -1);
    int ans = 0;
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < m; ++j) {
            ans = max(ans , solve(i , j));
        }
    }
    cout << ans << nl;
}
