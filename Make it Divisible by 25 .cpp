void TC() {
    string s;
    cin >> s;
    int n = s.size();
    int ans = inf;

    vector<string> v = {"00", "25", "50", "75"};

    for (string x : v) {
        int p2 = -1 , p1 = -1;
        for (int i = n - 1; i >= 0; --i) {
            if (s[i] == x[1]) {
                p2 = i;
                break;
            }
        }

        for (int i = p2 - 1; i >= 0; --i) {
            if (s[i] == x[0]) {
                p1 = i;
                break;
            }
        }
        if (p1 == -1) continue;

        ans = min(ans , (n - 1 - p2) + (p2 - p1 - 1));
    }

    cout << ans << nl;
}
