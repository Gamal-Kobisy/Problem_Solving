void solve() {
    int n , k;
    cin >> n >> k;
    vector<int>a(n) , pre(n + 1);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    sort(all(a));
    for(int i = 1 ; i <= n ; i++){
        pre[i] = pre[i - 1] + a[i - 1];
    }
    int ans = 0;
    for(int i = 0 ; i <= k ; i++){
        int cand = pre[n - i] - pre[(k - i) * 2];
        ans = max(ans , cand);
    }
    cout << ans << nl;
}
