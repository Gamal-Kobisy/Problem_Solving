const ll MOD = 1e9 + 7;

ll add(ll a, ll b)
{
    return ((a % MOD) + (b % MOD)) % MOD;
}

ll sub(ll a, ll b)
{
    return ((a % MOD) - (b % MOD) + MOD) % MOD;
}

ll mul(ll a, ll b)
{
    return ((a % MOD) * (b % MOD)) % MOD;
}
int memo[5001][5001];
int solve(int n , int m) {
    if (m < 0) return 0;
    if (n < 0) return 0;
    if (n == 0) return (m == 0);
    if (m == 0) return 1;
    int &ret = memo[n][m];
    if(~ret) return ret;
    ret = add(
            sub( mul(2, solve(n-1, m)) , solve(n-2, m) ),
            solve(n, m - n)
    );
    return ret;
}
