ll fact(int n) {
    ll res = 1;
    for (int i = 2; i <= n; i++) {
        res *= i;
    }
    return res;
}

ll nCr(int n, int r) {
    if (r > n || r < 0) return 0;
    return fact(n)/ (fact(r) * fact(n - r));
}

ll nPr(int n, int r) {
    if (r > n || r < 0) return 0;
    return fact(n) / fact(n - r);
}

ll starsBars(int n , int k){
    return nCr(n + k - 1 , k - 1);
}

