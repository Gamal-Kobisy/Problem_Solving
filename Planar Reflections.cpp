const ll MOD = 1e9 + 7;

ll add(ll a, ll b)
{
    return ((a % MOD) + (b % MOD)) % MOD;
}

ll sub(ll a, ll b)
{
    return ((a % MOD) - (b % MOD) + MOD) % MOD;
}

ll mul(ll a, ll b)
{
    return ((a % MOD) * (b % MOD)) % MOD;
}
int solve(int idx , int decay , int dir) {
    if(decay < 1) return 0;
    if(idx == -1 or idx == n) return 1;
    if(~dp[idx][decay][dir+1]) return dp[idx][decay][dir+1];
    int ret = add(solve(idx + dir , decay , dir) ,
            solve(idx - dir , decay - 1 , 0 - dir));
    dp[idx][decay][dir+1] = ret;
    return ret;
}
