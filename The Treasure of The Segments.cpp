void TC() {
    int n;
    cin >> n;
    vector<pii> seg(n);
    vector<int> L(n), R(n);
    for (int i = 0; i < n; ++i) {
        int l, r;
        cin >> l >> r;
        seg[i] = {l, r};
        L[i] = l;
        R[i] = r;
    }

    sort(all(L));
    sort(all(R));

    int ans = n;
    for (int i = 0; i < n; ++i) {
        int l = seg[i].fi;
        int r = seg[i].se;
        int left = lower_bound(all(R), l) - R.begin();
        int right = n - (upper_bound(all(L), r) - L.begin());
        ans = min(ans , n - (n - left - right));
    }

    cout << ans << nl;
}
