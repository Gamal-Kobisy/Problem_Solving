// LINK : https://codeforces.com/problemset/problem/1985/B

#include <bits/stdc++.h>
using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;

        int x = n;
        if (n % 2 == 0) {
            x = 2;
        }
        cout << x << "\n";
    }

    return 0;
}
