void TC() {
    int n;
    cin >> n;
    vector<int>a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    int l = 0 , r = 0;
    map<int , int>freq;
    int ans = 0;
    while(l <= r and r < n){
        freq[a[r]]++;
        r++;
        if(freq.size() == 1){
            ans = max(ans , freq.begin()->sc);
        }else if(freq.size() == 2 and freq.begin()->fr == 0){
            ans = max(ans , freq.begin()->sc + next(freq.begin())->sc);
        }else{
            while(l < r and not ((freq.size() == 1) or (freq.size() == 2 and freq.begin()->fr == 0))){
                freq[a[l]]--;
                if(not freq[a[l]]) freq.erase(freq.find(a[l]));
                l++;
            }
        }
    }
    cout << ans << nl;
}
