using ordered_set = tree<T, null_type, less_equal<T>, rb_tree_tag, tree_order_statistics_node_update>;

void TC() {
    int n;
    cin >> n;
    vector<pll> p(n);
    for (int i = 0; i < n; ++i) {
        int l, r;
        cin >> l >> r;
        p[i] = {l, r};
    }
    sort(allr(p));
    ordered_set<pll> os;
    long long ans = 0;
    int i = 0;
    while (i < n) {
        int j = i;
        while (j < n && p[j].first == p[i].first)
            ++j;
        for (int k = i; k < j; ++k) {
            ans += os.order_of_key({ p[k].second, -infLL});
        }
        for (int k = i; k < j; ++k) {
            os.insert({p[k].second, k});
        }
        i = j;
    }

    cout << ans << nl;
}
