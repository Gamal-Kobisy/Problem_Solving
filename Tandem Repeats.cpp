bool tandem(int l , int r){
    int sz = r - l + 1;
    if(sz & 1) return false;
    for (int i = l; i < (l + sz / 2); ++i) {
        if(s[i] != s[i + sz / 2] and s[i] != '?' and s[i + sz / 2] != '?') return false;
    }
    return true;
}

void TC() {
    cin >> s;
    n = s.size();
    int ans = 0;
    for (int l = 0; l < n; ++l) {
        for (int r = n - 1; r >= l + 1; --r) {
            if(tandem(l , r)){
                ans = max(ans , r - l + 1);
                break;
            }
        }
    }
    cout << ans << nl;
}
